def _task_from_payload(payload: Any) -> Task:
    """Reconstruct a :class:`Task` from the SSE source's published payload.

    The :class:`~lithos_loom.sources.lithos_event_stream.LithosEventStream`
    publishes the full Task shape (id, title, status, tags, metadata,
    claims, resolved_at), so this is just dict-lookups, not a re-fetch.
    """
    return Task(
        id=str(payload["id"]),
        title=str(payload.get("title", "")),
        status=str(payload.get("status", "")),
        tags=tuple(payload.get("tags") or ()),
        metadata=dict(payload.get("metadata") or {}),
        claims=tuple(payload.get("claims") or ()),
        resolved_at=_parse_resolved_at(payload.get("resolved_at")),
    )


def _parse_resolved_at(value: Any) -> datetime | None:
    """Defensive ISO-8601 parse for ``resolved_at`` from the SSE payload.

    Returns ``None`` for missing / malformed values so the handler can
    fall back to ``event.timestamp`` rather than crashing.
    """
    if value is None or value == "":
        return None
    if not isinstance(value, str):
        return None
    try:
        return datetime.fromisoformat(value)
    except ValueError:
        logger.warning(
            "obsidian-projection: ignoring malformed resolved_at=%r in payload",
            value,
        )
        return None


def _resolved_at_for(event: Event, task: Task) -> date:
    """Pick the canonical resolution date for a terminal event.

    Preference order (per PR #21 review issue 3):

    1. ``task.resolved_at`` from the SSE payload — this is Lithos's
       canonical timestamp (lithos#286 / PR #288), written by both
       ``complete_task`` and ``cancel_task`` when the task transitions
       to a terminal state. Stable across reconnect-replay, restart,
       and the bootstrap-resolved-discovery path.
    2. ``event.timestamp.astimezone().date()`` — the bus event's
       receive-at time. Fallback only; drifts vs. canonical for
       delayed/replayed events but matches the local-tz "today" the
       operator sees.

    Both branches return a local-tz ``date`` so the rendered marker
    matches the operator's calendar.
    """
    if task.resolved_at is not None:
        return task.resolved_at.astimezone().date()
    return event.timestamp.astimezone().date()


def _evict_expired(
    state: dict[str, _StateEntry],
    today: date,
    ttl_days: int,
) -> list[str]:
    """Drop state entries whose ``resolved_at < today - ttl_days``.

    Open entries (``resolved_at is None``) are never affected.
    Returns the list of evicted task IDs so the caller can decide
    whether the file content moved even when the triggering event
    itself didn't change the keyed entry.

    ``ttl_days`` is validated as ``>= 0`` by the config parser; the
    defensive check below is for direct-callsite robustness.
    """
    if ttl_days < 0:
        return []
    cutoff = today - timedelta(days=ttl_days)
    evicted: list[str] = []
    for tid, entry in list(state.items()):
        if entry.resolved_at is not None and entry.resolved_at < cutoff:
            del state[tid]
            evicted.append(tid)
    return evicted


def _render_file(state: dict[str, _StateEntry]) -> str:
    """Render the full file content from the state dict.

    Open and resolved entries are interleaved when sorted by task
    id; the user's existing Tasks-plugin queries differentiate via
    the checkbox state (``[ ]`` vs ``[x]``/``[-]``).

    Sorted by task id for deterministic output (cheap stability for
    tests and for US14's content-hash dedup at the file boundary).
    """
    if not state:
        return _FILE_HEADER + "\n"
    lines = [_FILE_HEADER, ""]
    for tid in sorted(state):
        lines.append(state[tid].line)
    lines.append("")
    return "\n".join(lines)


def _hash_existing_file(path: Path) -> bytes | None:
    """Compute the SHA-256 digest of ``path``'s current contents.

    Returns ``None`` when the file doesn't exist or isn't readable
    (cold start, permission flip, weird filesystem state). Used by
    ``make_handler`` to seed ``last_written_hash`` so a daemon
    restart that rebuilds in-memory state to the same content as the
    on-disk file results in zero disk writes at the convergent point
    (US14).
    """
    try:
        raw = path.read_bytes()
    except (FileNotFoundError, OSError):
        return None
    return hashlib.sha256(raw).digest()


